    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEliminarActionPerformed
    String IdentificadorDeExpediente = Expediente.getText();
    
    Expediente.setText("");
    
    Nodo<Expediente> nodoActual = this.pilaExpdiente.top;
    Nodo<Expediente> nodoAnterior = null;
    while (nodoActual != null) {
        Expediente expedienteActual = nodoActual.getValue();
        if (expedienteActual.getIdentificadorUnico().equals(IdentificadorDeExpediente)) {
            if (nodoAnterior == null) {
                this.pilaExpdiente.pop();
            } else {
                nodoAnterior.setNext(nodoActual.getNext());
            }
            LocalDateTime fechaEliminacion = LocalDateTime.now();
            String fechaHoraEliminacion = fechaEliminacion.format(DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss"));
            JOptionPane.showMessageDialog(this, "Se eliminó el expediente " + IdentificadorDeExpediente + " a las " + fechaHoraEliminacion, "Expediente eliminado", JOptionPane.INFORMATION_MESSAGE);
            agregarDatosATabla();
            break;
        }
        nodoAnterior = nodoActual;
        nodoActual = nodoActual.getNext();
    }

    }//GEN-LAST:event_BotonEliminarActionPerformed

    private void BotonTiempoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonTiempoActionPerformed
        String IdentificadorDeExpediente = Expediente.getText();
        Nodo<Expediente> nodoActual = this.pilaExpdiente.top;
        while (nodoActual != null) {
            Expediente expedienteActual = nodoActual.getValue();
            if (expedienteActual.getIdentificadorUnico().equals(IdentificadorDeExpediente)) {
                LocalDateTime fechaIngreso = expedienteActual.getFechaIngreso();
                LocalDateTime fechaActual = LocalDateTime.now();
                long duracionEnSegundos = java.time.Duration.between(fechaIngreso, fechaActual).getSeconds();
                long minutos = duracionEnSegundos / 60;
                long horas = minutos / 60;
                long dias = horas / 24;
            
                String duracion = String.format("Duración: %d días, %d horas, %d minutos y %d segundos", dias, horas % 24, minutos % 60, duracionEnSegundos % 60);
                JOptionPane.showMessageDialog(this, duracion, "Duración del expediente", JOptionPane.INFORMATION_MESSAGE);
                break;
            }
            nodoActual = nodoActual.getNext();
        }
    }//GEN-LAST:event_BotonTiempoActionPerformed
