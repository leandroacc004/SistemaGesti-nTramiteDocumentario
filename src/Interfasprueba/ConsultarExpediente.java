    private void BotonCambiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCambiarActionPerformed
        String IdentificadorDeExpediente = Expediente.getText();
        String dependenciaDestino = DependenciaDestino.getSelectedItem().toString();
        
        Nodo<Expediente> nodoActual = this.pilaExpdiente.top;
        while (nodoActual != null) {
            Expediente expedienteActual = nodoActual.getValue();
            //System.out.println("Expediente: " + expedienteActual.getIdentificadorUnico());
            //System.out.println(IdentificadorDeExpediente);
            if (expedienteActual.getIdentificadorUnico().equals(IdentificadorDeExpediente)) {
                expedienteActual.setDependencia(dependenciaDestino);
                JOptionPane.showMessageDialog(this, "Se actualizó la tabla", " ", JOptionPane.ERROR_MESSAGE);
                agregarDatosATabla();
                break;
            }
            nodoActual = nodoActual.getNext();
        }
    }//GEN-LAST:event_BotonCambiarActionPerformed

